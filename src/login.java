    private void btn_masukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_masukActionPerformed
        try {
            String sql = "SELECT * FROM akun WHERE user = '" +txt_user.getText() +"'AND password = '" +txt_pass.getText()+"'";
            java.sql.Connection conn = (Connection) koneksi.loginDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){
                if(txt_user.getText().equals(rs.getString("user")) && txt_pass.getText().equals(rs.getString("password")))  {
                    
                    this.setVisible(false);
                    new menu().setVisible(true);
                }
            }else{
                JOptionPane.showMessageDialog(null,"user atau password salah");
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_btn_masukActionPerformed

